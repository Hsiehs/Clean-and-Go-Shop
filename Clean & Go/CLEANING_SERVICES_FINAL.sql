CREATE SCHEMA IF NOT EXISTS CleaningServices DEFAULT CHARACTER SET utf8 ;
USE CleaningServices;

CREATE TABLE IF NOT EXISTS VENDOR (
  ID INT NOT NULL,
  NAME VARCHAR(45) NOT NULL,
  ADDRESS VARCHAR(45) NOT NULL,
  PHONE CHAR(10) NOT NULL,
  CURRENT_BALANCE DOUBLE NOT NULL,
  PRIMARY KEY (ID)
);

CREATE TABLE IF NOT EXISTS PO_RECORD (
  PO_N INT NOT NULL,
  VENDOR_ID INT NOT NULL,
  PROD_NUM VARCHAR(25) NOT NULL,
  PROD_NAME VARCHAR(45) NOT NULL,
  QUANTITY DOUBLE NOT NULL,
  PRICE VARCHAR(45) NOT NULL,
  UNITS VARCHAR(45) NOT NULL,
  DESCRIPTION VARCHAR(45) NOT NULL,
  PRODUCT_CATEGORY VARCHAR(45) NULL,
  PRIMARY KEY (PO_N),
  FOREIGN KEY (VENDOR_ID) REFERENCES VENDOR (ID) ON DELETE CASCADE ON UPDATE CASCADE
  );

CREATE TABLE IF NOT EXISTS INVOICE (
  TRANSACTION_ID VARCHAR(25) NOT NULL,
  PO_N INT NOT NULL,
  DATE DATE NOT NULL,
  AMOUNT_DUE DOUBLE NOT NULL,
  DUE_DATE DATE NOT NULL,
  DELIVERY_DATE DATE NOT NULL,
  PAID TINYINT NOT NULL,
  PRIMARY KEY (TRANSACTION_ID),
  FOREIGN KEY (PO_N) REFERENCES PO_RECORD (PO_N) ON DELETE CASCADE ON UPDATE CASCADE
  );

CREATE TABLE IF NOT EXISTS CLEANING_SUPPLIES (
  PRODUCT_CATEGORY VARCHAR(25) NOT NULL,
  TRANSACTION_ID VARCHAR(25) NOT NULL,
  CURRENT_INVENTORY DOUBLE NOT NULL,
  SAFETY_STOCK_LEVEL DOUBLE NOT NULL,
  PRIMARY KEY (PRODUCT_CATEGORY),
  FOREIGN KEY (TRANSACTION_ID) REFERENCES INVOICE (TRANSACTION_ID) ON DELETE CASCADE ON UPDATE CASCADE
  );

CREATE TABLE IF NOT EXISTS CUSTOMER (
  ID INT NOT NULL,
  NAME VARCHAR(45) NOT NULL,
  DATE DATE NOT NULL,
  ADDRESS VARCHAR(45) NOT NULL,
  EMAIL VARCHAR(45) NOT NULL,
  PHONE CHAR(10) NOT NULL,
  CREDIT_CARD_N CHAR(16) NOT NULL,
  CURRENT_BALANCE DOUBLE NOT NULL,
  PRIMARY KEY (ID)
  );


CREATE TABLE IF NOT EXISTS EMPLOYEE (
  SSN CHAR(9) NOT NULL,
  NAME VARCHAR(45) NOT NULL,
  ADDRESS VARCHAR(45) NOT NULL,
  GENDER VARCHAR(6) NULL,
  HIRE_DATE DATE NOT NULL,
  SALARY DOUBLE NOT NULL,
  POSITION VARCHAR(45) NOT NULL,
  PRIMARY KEY (SSN)
  );

CREATE TABLE IF NOT EXISTS EQUIPMENT (
  INTERNAL_ID INT NOT NULL,
  TRANSACTION_ID VARCHAR(25) NOT NULL,
  BRAND_NAME VARCHAR(45) NOT NULL,
  AVAILABLE TINYINT NOT NULL,
  MAINT_TYPE VARCHAR(45) NOT NULL,
  REPLACEMENT_DATE DATE NOT NULL,
  PRIMARY KEY (INTERNAL_ID),
  FOREIGN KEY (TRANSACTION_ID) REFERENCES INVOICE (TRANSACTION_ID)
  );
  
CREATE TABLE IF NOT EXISTS SERVICE (
  ID INT NOT NULL,
  NAME VARCHAR(45) NOT NULL,
  DESCRIPTION VARCHAR(45) NOT NULL,
  RATE_CHARGED DOUBLE NOT NULL,
  DURATION TIME NOT NULL,
  PRIMARY KEY (ID)
  );


CREATE TABLE IF NOT EXISTS SATISFACTION_SCALE (
  NUMBER INT NOT NULL,
  DESCRIPTION VARCHAR(100) NOT NULL,
  PRIMARY KEY (NUMBER)
  );

CREATE TABLE IF NOT EXISTS SERVICE_RECORD (
  SERV_RECORD_N INT NOT NULL,
  SERVICE_ID INT NOT NULL,
  CUSTOMER_ID INT NOT NULL,
  DATE DATE NOT NULL,
  SATISFACTION INT NULL,
  AMOUNT_PAID TINYINT NOT NULL,
  ITEM_DESCRIPTION VARCHAR(45) NULL,
  PRIMARY KEY (SERV_RECORD_N, DATE),
  FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER (ID) ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (SERVICE_ID) REFERENCES SERVICE (ID) ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (SATISFACTION) REFERENCES SATISFACTION_SCALE (NUMBER) ON DELETE CASCADE ON UPDATE CASCADE
  );

CREATE TABLE IF NOT EXISTS WEEK (
  WEEK_DAY_CODE CHAR(2) NOT NULL,
  SCHEDULE_TYPE VARCHAR(45) NOT NULL,
  START_TIME TIME NOT NULL,
  END_TIME TIME NOT NULL,
  PRIMARY KEY (WEEK_DAY_CODE, SCHEDULE_TYPE)
  );

CREATE TABLE IF NOT EXISTS SCHEDULE (
  EMPLOYEE_SSN CHAR(9) NOT NULL, -- FK->
  DATE DATE NOT NULL,
  SERVICE_ID INT NOT NULL, -- FK->
  WEEK_DAY_CODE CHAR(2) NOT NULL, -- FK->
  SCHEDULE_TYPE VARCHAR(45) NOT NULL, -- FK->
  PRIMARY KEY (EMPLOYEE_SSN),
  FOREIGN KEY (EMPLOYEE_SSN) REFERENCES EMPLOYEE (SSN) ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (SERVICE_ID) REFERENCES SERVICE (ID) ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (WEEK_DAY_CODE, SCHEDULE_TYPE) REFERENCES WEEK (WEEK_DAY_CODE, SCHEDULE_TYPE) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS EQUIP_SCHEDULE (
  SERVICE_RECORD_N INT NOT NULL,
  EQUIPMENT_ID INT NOT NULL,
  VENDOR_ID INT NOT NULL,
  DATE DATE NOT NULL,
  START_TIME TIME NOT NULL,
  END_TIME TIME NOT NULL,
  NEXT_MAINT_DATE DATE NOT NULL,
  PRIMARY KEY (SERVICE_RECORD_N),
  FOREIGN KEY (EQUIPMENT_ID) REFERENCES EQUIPMENT (INTERNAL_ID) ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (VENDOR_ID) REFERENCES VENDOR (ID) ON DELETE CASCADE ON UPDATE CASCADE
 );

CREATE TABLE IF NOT EXISTS SERVICE_USES_CLEANING_SUPPLIES (
  SERVICE_ID INT NOT NULL,
  PRODUCT_CATEGORY VARCHAR(25) NOT NULL,
  QUANTITY DOUBLE NOT NULL,
  PRIMARY KEY (SERVICE_ID, PRODUCT_CATEGORY),
  FOREIGN KEY (SERVICE_ID) REFERENCES SERVICE (ID) ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (PRODUCT_CATEGORY) REFERENCES CLEANING_SUPPLIES (PRODUCT_CATEGORY) ON DELETE CASCADE ON UPDATE CASCADE
  );

CREATE TABLE IF NOT EXISTS SERVICE_USES_EQUIPMENT (
  SERVICE_ID INT NOT NULL,
  EQUIPMENT_INTERNAL_ID INT NOT NULL,
  DURATION_IN_SERVICE DOUBLE NOT NULL,
  USE_DATE DATE NOT NULL,
  RECORD_N INT NOT NULL,
  PRIMARY KEY (SERVICE_ID, EQUIPMENT_INTERNAL_ID),
  FOREIGN KEY (SERVICE_ID) REFERENCES SERVICE (ID) ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (EQUIPMENT_INTERNAL_ID) REFERENCES EQUIPMENT (INTERNAL_ID) ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (RECORD_N, USE_DATE) REFERENCES SERVICE_RECORD (SERV_RECORD_N, DATE) ON DELETE CASCADE ON UPDATE CASCADE

); 

